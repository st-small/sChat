import UIKit

extension UIFont {
    
    public static func avenir20() -> UIFont? {
        return UIFont(name: "avenir", size: 20)
    }
    
    public static func avenir26() -> UIFont? {
        return UIFont(name: "avenir", size: 26)
    }
    
    public static func laoSangamMN20() -> UIFont? {
        return UIFont.init(name: "Lao Sangam MN", size: 20)
    }
    
    public static func laoSangamMN18() -> UIFont? {
        return UIFont.init(name: "Lao Sangam MN", size: 18)
    }
}
